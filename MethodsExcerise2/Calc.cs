using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodsExcerise2
{
    internal class Calc
    {
        public static int Sum(int x,int y)
        {
            return x + y;
        }
        public static int Multi(int x, int y)
        {
            return x * y;        
        }
        public static int sub(int x, int y)

        {
            return x - y;
        }
        public static int div(int x, int y)
        {
            return x / y;

        }

        public static int Sums(int a, int b, int c, int d, int e)
        {
            return a + b + c + d + e;
        }
        
    }
